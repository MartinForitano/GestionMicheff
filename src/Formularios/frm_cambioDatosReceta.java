    public void revertirCampos() {
        Connection con = null;
        Statement st = null;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/gestion_michef", user, password);
            st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from ingredientesRecetas where idingredientesRecetas = " + codigo);
            while (rs.next()) {
                jTextField1.setText(rs.getString(2));
                jTextField4.setText(rs.getString(3));
            }
        } catch (Exception e) {
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_cambioDatosReceta.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (st != null) {
                try {
                    st.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_cambioDatosReceta.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

    public void cambiarDatos() {
        Connection con = null;
        PreparedStatement st = null;
        try {
            String nom = jTextField1.getText();
            String medida = jTextField4.getText();
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/gestion_michef", user, password);
            st = con.prepareStatement("update ingredientesRecetas set descripcion = ?, medida = ? where idingredientesRecetas = ?");
            st.setString(1, nom);
            st.setString(2, medida);
            st.setInt(3, codigo);
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "El ingrediente ha sido modificado", "Exito", JOptionPane.INFORMATION_MESSAGE);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frm_cambioDatos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(frm_cambioDatos.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_cambioDatosReceta.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (st != null) {
                try {
                    st.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_cambioDatosReceta.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

