    public void cambiarDatos() {
        Connection con = null;
        PreparedStatement pst = null;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/gestion_michef", user, password);
            pst = con.prepareStatement("update proveedores set descripcion = ?, numeroContacto = ?, cuit = ? where idproveedores = ?");
            pst.setString(1, jTextField2.getText());
            pst.setInt(2, Integer.parseInt(jTextField3.getText()));
            pst.setInt(3, Integer.parseInt(jTextField4.getText()));
            pst.setInt(4, Integer.parseInt(jTextField1.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "El proveedor ha sido modificado", "Exito", JOptionPane.INFORMATION_MESSAGE);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frm_menu_almacen.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(frm_menu_almacen.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_modificarProveedores.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (pst != null) {
                try {
                    pst.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_modificarProveedores.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

    public void revertirCampos() {
        Connection con = null;
        Statement st = null;
        int cod = Integer.parseInt(jTextField1.getText());
        Integer contacto = 0, cuit = 0;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/gestion_michef", user, password);
            st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from proveedores where idproveedores = " + cod);
            while (rs.next()) {
                jTextField2.setText(rs.getString(2));
                contacto = rs.getInt(3);
                cuit = rs.getInt(4);
                jTextField3.setText(contacto.toString());
                jTextField4.setText(cuit.toString());
            }
        } catch (ClassNotFoundException | SQLException e) {
        } finally {
            if (con != null) {
                try {
                    con.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_modificarProveedores.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (st != null) {
                try {
                    st.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_modificarProveedores.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

