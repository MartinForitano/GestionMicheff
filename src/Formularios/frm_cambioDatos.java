            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/gestion_michef", user, password);
            st = con.prepareStatement("update ingredientes set descripcion = ?, idcategoria = ?, cantidad = ?, medida = ?, puntoReposicion = ? where idingredientes = ?");
